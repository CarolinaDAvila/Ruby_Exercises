# nil is a way to express "nothing", "completely empty", "not any specific type"
puts "Hello, World!"

# Use .nil? to check if something is nil
"Hello, World".nil?

# When used in an expression, such as an if statement, it is represented as false. 
if nil
  puts "Hello, World!"
end 

# Condition example : 1 is a false value 
 if 1
  puts "Hello World!"
end 

# False and nil are not equivalent 
false == nil

