# String : list of characters in a specific sequence.
# A string can be surrounded by either single or double quotes.

# Example 1: with double quotes
puts "The Man said, 'Hi there!'"

# Example 2: with single quotes and escaping
puts 'The man said, \'Hi there!\'' 

# String interpolation merges Ruby code with strings, ONLY works with double quotes.

# Example 3:

a = 'ten'

puts "My favorite number is #{a}!"

# Remember the basic syntax is #{ruby expression} 