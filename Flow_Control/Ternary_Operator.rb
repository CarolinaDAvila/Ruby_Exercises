# Ternary operator makes a quick 'if/else' statement in one line. 
  # First, the computer evaluates what is to the left of the ?. if the expression is true, the code directly to the right of the ? gets executed
    # If the code on the left of the ? is false, then the code will directly go to the right of the : and get executed. 
true ? "this is true" : "this is not true"
    # will return "this is true"
false ? "this is true" : "this is not true"
    # will return "this is not true"
