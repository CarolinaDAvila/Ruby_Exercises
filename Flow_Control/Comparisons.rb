#Comparison Operators always return a boolean value. 
  #Example using 'less than' and 'greater than'
4 < 5
  # will return true
4 > 5
  # will return false
  
  #Example using 'less than or equal to' and 'greater than or equal to'
4 <= 5
    # will return true
4 >= 5
    # will return false

  #Example using 'equal to' operator
5 == 4
      # will return false
5 == 5
      # will return true 
'5' == 5
      # will return as false
  
  #Example using the 'not equal to' operator
4 != 5
      # will return true
4 != 4
      # will return false 
